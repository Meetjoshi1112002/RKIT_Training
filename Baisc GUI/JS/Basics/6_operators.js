// Types of logical operator in js

// == - equal to (by value)
console.log("5" == 5);

// === - equal to (by value and type)
console.log("5" === 5);

// != - not equal to
console.log("5" != 5);

// !== - not equal to (by value and type)
console.log("5" !== 5);

// > - greater than
// < - less than
// >= - greater than or equal to
// <= - less than or equal to

// Basic operator:
// 1. Arithmetic +
console.log(5 + "5"); // 55 
// 2. Assignment =

// 3. Modulus %
console.log(typeof(5 ** "2")); // such mathematical operator always returns number (other than addition)

